
public class MySerieDO {
	
	private int id;
	private String nom, annee;
	private boolean productionOriginale;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getAnnee() {
		return annee;
	}
	public void setAnnee(String annee) {
		this.annee = annee;
	}
	public boolean isProduction_originale() {
		return productionOriginale;
	}
	public void setProduction_originale(boolean production_originale) {
		this.productionOriginale = production_originale;
	}
	

}
